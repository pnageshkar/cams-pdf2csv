"""
cams_parser.py - Extract mutual fund transaction data from CAMS Consolidated Account Statements (CAS) in PDF format.

This module reads a password-protected (or unprotected) PDF file generated by CAMS (Computer Age Management Services)
and KFintech, parses its text content line-by-line, and extracts structured transaction records.

=== PDF STRUCTURE (CAMS CAS) ===

A CAMS CAS PDF typically has the following layout:

    Page 1:
        - Header: Statement version info (reversed text), title, date range
        - Investor Details: Name, address, email, phone (arranged in two columns alongside
          a description paragraph)
        - Portfolio Summary table
        - Footer: PAN, KYC status

    Subsequent Pages (one or more per folio):
        - "Folio No: XXXXXXX / XX  PAN: XXXXXXXXX  KYC: OK  PAN: OK"
        - Investor name (on next line)
        - Fund name line: "SCHEME_CODE-Fund Name ... (Non-Demat) - ISIN: INFxxxxxxxxx(Advisor: ...) Registrar : CAMS"
        - Nominee info
        - "Opening Unit Balance: X,XXX.XXX"
        - Transaction rows:  "DD-Mon-YYYY  Description  Amount  Units  NAV  UnitBalance"
        - Closing balance, NAV, cost/market value info
        - Load structure notes

=== KEY CHALLENGES ===

1. **Column Merging**: pdfplumber extracts text from multi-column PDF layouts, which can
   cause text from adjacent columns to merge into a single line, producing garbled output.
   This is especially problematic for ISIN codes, which may be truncated or split across lines.

2. **Fund Name Wrapping**: Long fund names (especially those with "formerly known as" clauses)
   may span multiple PDF lines and need to be buffered and joined.

3. **Non-Transaction Lines**: The PDF contains many informational lines (nominee info, cost values,
   load structures, KYC info, etc.) that must be skipped during transaction extraction.

=== ISIN EXTRACTION STRATEGY ===

Indian mutual fund ISINs are exactly 12 alphanumeric characters starting with "INF".
Due to PDF text extraction issues, ISINs may be truncated or missing. This module uses
a three-layer approach to maximize ISIN recovery:

    Layer 1 - Inline extraction with look-ahead:
        When the fund name line is parsed, extract the ISIN from the same line.
        If the ISIN is partial (< 12 chars), look ahead up to 3 subsequent lines
        and append their leading alphanumeric characters to complete it.

    Layer 2 - Secondary search on nearby lines:
        If Layer 1 fails, scan up to 5 subsequent non-transaction lines for "ISIN:" patterns.
        Also try concatenating adjacent lines to recover ISINs split across line breaks.

    Layer 3 - Pre-scan lookup table:
        Before main extraction, do a quick first pass over all pages to build a
        {scheme_code: ISIN} lookup table from lines where ISINs appear intact.
        Use this as a final fallback when Layers 1 and 2 fail.

=== OUTPUT ===

Each extracted transaction is a dictionary with these keys:
    - Investor Name: Full name from PDF header (same for all transactions)
    - Folio Number: The folio number under which the fund is held
    - Fund Name: Scheme code + fund name (e.g., "H02T-HDFC Flexi Cap Fund - Direct Plan - Growth Option")
    - ISIN: 12-character International Securities Identification Number
    - Transaction: Description of the transaction (e.g., "Switch-In - From ...")
    - Transaction Type: "Purchase" (positive amount) or "Redemption" (negative amount)
    - Date: Transaction date in DD-Mon-YYYY format
    - Units: Number of units transacted (Decimal)
    - NAV: Net Asset Value on transaction date (Decimal)
    - Amount: Transaction amount in INR (Decimal, negative for redemptions)

Dependencies:
    - pdfplumber: PDF text extraction
    - pdfminer.six: PDF password validation (used by pdfplumber internally, also used directly)
"""

import re
import csv
from decimal import Decimal, InvalidOperation
import io
from typing import Optional, Union
import traceback
import pdfplumber
from pdfminer.pdfparser import PDFParser
from pdfminer.pdfdocument import PDFDocument, PDFPasswordIncorrect
from pdfminer.pdfpage import PDFPage


