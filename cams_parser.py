def convert_to_csv_string(data_list):
    """
    Convert a list of transaction dictionaries to a CSV-formatted string.

    The CSV includes a header row and uses the following column order:
        Investor Name, Folio Number, Fund Name, ISIN, Transaction,
        Transaction Type, Date, Units, NAV, Amount

    Args:
        data_list: List of dicts as returned by extract_transactions_from_pdf().

    Returns:
        A string containing the full CSV content (including header),
        or an empty string if data_list is empty.
    """
    if not data_list:
        return ""
    output = io.StringIO()
    fieldnames = [
        "Investor Name",
        "Folio Number",
        "Fund Name",
        "ISIN",
        "Transaction",
        "Transaction Type",
        "Date",
        "Units",
        "NAV",
        "Amount",
    ]
    writer = csv.DictWriter(
        output, fieldnames=fieldnames, extrasaction="ignore", restval=""
    )
    writer.writeheader()
    writer.writerows(data_list)
    return output.getvalue()


