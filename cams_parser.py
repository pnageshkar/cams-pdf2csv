def extract_transactions_from_pdf(pdf_file_or_path, pdf_password=""):
    """
    Extract mutual fund transactions from a CAMS Consolidated Account Statement PDF.

    This is the main entry point for the parser. It performs:
        1. PDF password validation
        2. Pre-scan for ISIN lookup table (Layer 3 fallback)
        3. Page-by-page text extraction and line-by-line parsing
        4. Post-processing (stamp duty merging, transaction type classification)

    The parsing uses a state machine approach:
        - State -1 (IDLE): Waiting for a "Folio No:" line to start a new section.
        - State  0 (EXPECTING_FUND_NAME): Folio found; looking for the fund name line
          (typically 2 lines after the folio line).

    Args:
        pdf_file_or_path: Path to the CAMS CAS PDF file, or a file-like byte stream.
        pdf_password: Password for the PDF. Defaults to empty string (unprotected).

    Returns:
        tuple: (data_list, status_message)
            - On success: (list of transaction dicts, "Success")
            - On failure: (None or [], error_message_string)
    """
    extracted_data_raw = []

    # --- Layer 3 Setup: Build ISIN lookup table from a quick pre-scan ---
    isin_lookup = prescan_isin_lookup(pdf_file_or_path, pdf_password)

    # =====================================================================
    # REGEX PATTERNS
    # =====================================================================

    # Matches "Folio No: 12345678 / 19" and captures the folio number
    folio_regex = re.compile(r"^Folio No:\s*([\w/]+)")

    # Matches fund name lines like:
    #   "B51Z-Aditya Birla Sun Life Flexi Cap Fund - Growth-Direct Plan (Non-Demat) - ISIN: ..."
    # Captures the "SchemeCode-FundName" portion up to known delimiters.
    # The delimiters (non-capturing group) are:
    #   - " - ISIN:" (most common)
    #   - "(formerly known as" (renamed funds)
    #   - "(Non-Demat)" (demat status)
    #   - "(Advisor:" (advisor info)
    #   - End of line (fallback for truncated lines)
    fund_name_regex = re.compile(
        r"^(?P<FundNameCode>[A-Z0-9]+-[A-Za-z0-9\s().'/,\-:&]+?)"
        r"(?:\s*-\s*ISIN:|\s*\(formerly known as|\s*\(Non-Demat\)|$|\s*\(Advisor:)",
        re.IGNORECASE,
    )

    # Full transaction line with all 4 numeric columns:
    #   "DD-Mon-YYYY  Description  Amount  Units  NAV  UnitBalance"
    transaction_line_regex_full = re.compile(
        r"^(?P<Date>\d{2}-[A-Za-z]{3}-\d{4})\s+"
        r"(?P<Transaction>.+?)\s+"
        r"(?P<Amount>[\d,.-]+|\([\d,.-]+\)|--)?\s+"
        r"(?P<Units>[\d,.-]+|\([\d,.-]+\)|--)?\s+"
        r"(?P<NAV>[\d,.-]+|\([\d,.-]+\)|--)?\s+"
        r"(?P<UnitBalance>[\d,.-]+|\([\d,.-]+\)|--)$"
    )

    # Partial transaction line with only an amount (e.g., stamp duty, STT):
    #   "DD-Mon-YYYY  Description  Amount"
    transaction_line_regex_amount_only = re.compile(
        r"^(?P<Date>\d{2}-[A-Za-z]{3}-\d{4})\s+"
        r"(?P<Transaction>.+?)\s+"
        r"(?P<Amount>[\d,.-]+|\([\d,.-]+\)|--)$"
    )

    # Detects if a line starts with a date pattern ("DD-Mon-YYYY")
    date_pattern_start = re.compile(r"^\d{2}-[A-Za-z]{3}-\d{4}")

    # Detects lines starting with a scheme code (e.g., "H02T-", "RMFGFAGG-")
    scheme_code_start_pattern = re.compile(r"^[A-Z0-9]+-")

    # =====================================================================
    # STATE VARIABLES
    # =====================================================================

    current_folio_number = None  # Current folio being processed
    current_fund_name_raw = None  # Raw fund name (scheme code + name)
    current_isin = None  # ISIN for the current fund
    investor_name = None  # Extracted once from page 1

    # Fund name detection state machine:
    #   -1 = IDLE (not looking for a fund name)
    #    0 = EXPECTING_FUND_NAME (folio found, looking for fund name line)
    fund_name_detection_state = -1
    fund_name_buffer = []  # Accumulates multi-line fund names
    lines_since_folio_for_fund_name = 0  # Counter to know when to expect the fund name

    # Secondary ISIN search: counts down lines to search after fund name detection
    isin_search_lines_remaining = 0

    # =====================================================================
    # PASSWORD VALIDATION
    # =====================================================================

    valid = is_pdf_password_valid(pdf_file_or_path, pdf_password)
    if valid is False:
        return None, "Incorrect password provided for PDF."
    elif valid is None:
        return None, "The selected file is not a valid PDF."

    # =====================================================================
    # MAIN EXTRACTION LOOP
    # =====================================================================

    try:
        with pdfplumber.open(pdf_file_or_path, password=pdf_password) as pdf:
            for page_num, page in enumerate(pdf.pages):
                # Extract text from each page with tolerances that work well for CAMS PDFs.
                # layout=False prevents pdfplumber from trying to reconstruct multi-column layouts,
                # which actually produces cleaner output for our line-by-line parsing.
                text_content = page.extract_text(
                    x_tolerance=3, y_tolerance=3, layout=False, keep_blank_chars=True
                )
                if not text_content:
                    continue
                lines = text_content.split("\n")

                for i, raw_line_from_pdf in enumerate(lines):
                    line = raw_line_from_pdf.strip()
                    if not line:
                        continue

                    # --- Investor Name Extraction (Page 1 only) ---
                    # On the first page, the investor's name appears on the line
                    # that also contains "balances and valuation" (part of the
                    # two-column layout where the left column has investor details
                    # and the right has the statement description).
                    # Example line: "Prasad Yeshwant Nageshkar balances and valuation..."
                    if page_num == 0 and not investor_name:
                        if "balances and valuation" in line:
                            investor_name = line.split("balances and valuation")[
                                0
                            ].strip()

                    # --- Folio Detection ---
                    # When we encounter "Folio No: XXXXX", we reset all fund-level
                    # state and begin looking for the fund name on subsequent lines.
                    folio_match = folio_regex.match(line)
                    if folio_match:
                        current_folio_number = folio_match.group(1)
                        current_fund_name_raw = None
                        current_isin = None
                        fund_name_buffer = []
                        fund_name_detection_state = 0  # Start looking for fund name
                        lines_since_folio_for_fund_name = 0
                        isin_search_lines_remaining = 0
                        continue

                    # --- Fund Name Detection (State 0) ---
                    # After finding a folio, we expect the fund name line to appear
                    # approximately 2 lines later (line 1 is usually the investor name,
                    # line 2+ is the fund name starting with a scheme code like "H02T-").
                    if (
                        fund_name_detection_state == 0
                        and current_folio_number
                        and not current_fund_name_raw
                    ):
                        lines_since_folio_for_fund_name += 1

                        if lines_since_folio_for_fund_name >= 2:
                            # Buffer lines that look like fund name lines (start with scheme code)
                            # or continue buffering if we already started.
                            if (
                                scheme_code_start_pattern.match(line)
                                or fund_name_buffer
                            ):
                                fund_name_buffer.append(raw_line_from_pdf)

                                # Join all buffered lines and try to match the fund name regex
                                combined_buffered_line = " ".join(
                                    fund_name_buffer
                                ).strip()
                                fund_name_match = fund_name_regex.match(
                                    combined_buffered_line
                                )

                                if fund_name_match:
                                    # --- Fund name successfully matched ---
                                    current_fund_name_raw = fund_name_match.group(
                                        "FundNameCode"
                                    ).strip()

                                    # --- Layer 1: ISIN extraction with look-ahead ---
                                    # Try to extract ISIN from the buffered fund name line.
                                    current_isin = extract_isin_from_text(
                                        combined_buffered_line
                                    )

                                    # If ISIN is partial (< 12 chars), the remaining characters
                                    # may have wrapped to the next line(s) due to PDF column merging.
                                    # Look ahead and append leading alphanumeric chars to complete it.
                                    # Example: Line has "ISIN: INF109K011O", next line starts with
                                    #          "5(Advisor:..." → append "5" to get "INF109K011O5"
                                    if current_isin and not is_valid_isin(current_isin):
                                        chars_needed = 12 - len(current_isin)
                                        for look_ahead in range(1, 4):
                                            if i + look_ahead < len(lines):
                                                next_line_text = lines[
                                                    i + look_ahead
                                                ].strip()
                                                leading = re.match(
                                                    r"^([A-Z0-9]+)", next_line_text
                                                )
                                                if leading:
                                                    current_isin += leading.group(1)[
                                                        :chars_needed
                                                    ]
                                                    if is_valid_isin(current_isin):
                                                        break
                                                    chars_needed = 12 - len(
                                                        current_isin
                                                    )

                                    fund_name_detection_state = -1  # Back to IDLE
                                    fund_name_buffer = []

                                    # If ISIN is still incomplete, activate Layer 2 search
                                    if not is_valid_isin(current_isin):
                                        isin_search_lines_remaining = 5

                            # If no scheme-code-like line appeared within the expected window,
                            # give up on finding the fund name for this folio.
                            elif (
                                not fund_name_buffer
                                and lines_since_folio_for_fund_name > 3
                            ):
                                fund_name_detection_state = -1

                        # Safety timeout: if we've looked at 5+ lines without finding
                        # a fund name, reset to avoid getting stuck
                        if (
                            lines_since_folio_for_fund_name > 5
                            and fund_name_detection_state == 0
                        ):
                            fund_name_detection_state = -1
                            fund_name_buffer = []

                        # While still in fund-name-detection mode (or just finished it
                        # but the current line isn't a transaction), skip to the next line.
                        if fund_name_detection_state == 0 or (
                            fund_name_detection_state == -1
                            and not date_pattern_start.match(line)
                        ):
                            continue

                    # --- Layer 2: Secondary ISIN Search ---
                    # If the fund name was found but the ISIN is still missing/incomplete,
                    # scan subsequent non-transaction lines for an "ISIN:" pattern.
                    # This handles cases where the ISIN appears on a separate line
                    # (e.g., continuation of "formerly known as..." text) or when the
                    # ISIN is split across two adjacent lines.
                    if (
                        isin_search_lines_remaining > 0
                        and not date_pattern_start.match(line)
                    ):
                        isin_search_lines_remaining -= 1
                        # Try extracting ISIN from this line
                        candidate = extract_isin_from_text(line)
                        if is_valid_isin(candidate):
                            current_isin = candidate
                            isin_search_lines_remaining = 0
                        else:
                            # Try concatenating with the next line to recover split ISINs
                            if i + 1 < len(lines):
                                combined = line + lines[i + 1].strip()
                                candidate = extract_isin_from_text(combined)
                                if is_valid_isin(candidate):
                                    current_isin = candidate
                                    isin_search_lines_remaining = 0
                        continue
                    elif isin_search_lines_remaining > 0 and date_pattern_start.match(
                        line
                    ):
                        # Transaction lines have started — stop ISIN search
                        isin_search_lines_remaining = 0
                        # --- Layer 3: Lookup table fallback ---
                        # If ISIN is still invalid, try the pre-scanned lookup table.
                        # Extract the scheme code (first part before "-") from the fund name.
                        if not is_valid_isin(current_isin) and current_fund_name_raw:
                            scheme_code = current_fund_name_raw.split("-")[0]
                            if scheme_code in isin_lookup:
                                current_isin = isin_lookup[scheme_code]

                    # --- Prepare fund name for transaction records ---
                    cleaned_fund_name_for_tx = remove_parenthetical_parts(
                        current_fund_name_raw
                    )

                    # Skip this line if we don't have the required context
                    # (must have both folio number and fund name to record transactions)
                    if not current_folio_number or not cleaned_fund_name_for_tx:
                        continue

                    # --- Skip Non-Transaction Lines ---
                    # The PDF contains many informational lines that look like they could
                    # be data but aren't transactions. Skip lines matching these keywords.
                    skip_keywords = [
                        "our mission",
                        "consolidated account statement",
                        "email id:",
                        "phone res:",
                        "phone off:",
                        "mobile:",
                        "this consolidated account statement is brought to you",
                        "if you find any folios missing",
                        "this statement may not reflect the complete information",
                        "portfolio summary",
                        "cost value",
                        "closing unit balance:",
                        "nav on ",
                        "total cost value:",
                        "market value on ",
                        "entry load:",
                        "exit load:",
                        "important note -",
                        "wef ",
                        "gst identification number",
                        "kyc:",
                        "pan:",
                        "registrar:",
                        "nominee ",
                        "page ",
                        r"camscasws-\d+",
                        "date transaction amount units nav unit",
                        "total",
                    ]
                    line_lower = line.lower()
                    should_skip_line = False
                    if (
                        "*** no transactions during this statement period ***"
                        in line_lower
                    ):
                        should_skip_line = True
                    else:
                        for keyword in skip_keywords:
                            if re.search(
                                r"(?:^|\s)" + re.escape(keyword) + r"(?:$|\s)",
                                line_lower,
                                re.IGNORECASE,
                            ):
                                # Special handling for "total": only skip if the line
                                # doesn't start with a date (because transaction
                                # descriptions can contain the word "total")
                                if keyword == "total" and not date_pattern_start.match(
                                    line
                                ):
                                    should_skip_line = True
                                    break
                                elif keyword != "total":
                                    should_skip_line = True
                                    break
                    if should_skip_line:
                        continue

                    # --- Transaction Line Parsing ---
                    # Only process lines that start with a date pattern ("DD-Mon-YYYY").
                    # Try the full regex first (Date, Transaction, Amount, Units, NAV, Balance),
                    # then fall back to amount-only regex for lines like stamp duty.
                    if date_pattern_start.match(line):
                        # Try full transaction line (all numeric columns present)
                        tx_match_full = transaction_line_regex_full.match(line)
                        if tx_match_full:
                            data = tx_match_full.groupdict()
                            extracted_data_raw.append(
                                {
                                    "Investor Name": investor_name,
                                    "Folio Number": current_folio_number,
                                    "Fund Name": cleaned_fund_name_for_tx,
                                    "ISIN": current_isin,
                                    "Date": data["Date"],
                                    "Transaction": data["Transaction"].strip(),
                                    "Amount": clean_numeric_value(data["Amount"]),
                                    "Units": clean_numeric_value(data["Units"]),
                                    "NAV": clean_numeric_value(data["NAV"]),
                                }
                            )
                            continue

                        # Try amount-only transaction line (stamp duty, STT, etc.)
                        tx_match_amount_only = transaction_line_regex_amount_only.match(
                            line
                        )
                        if tx_match_amount_only:
                            data = tx_match_amount_only.groupdict()
                            extracted_data_raw.append(
                                {
                                    "Investor Name": investor_name,
                                    "Folio Number": current_folio_number,
                                    "Fund Name": cleaned_fund_name_for_tx,
                                    "ISIN": current_isin,
                                    "Date": data["Date"],
                                    "Transaction": data["Transaction"].strip(),
                                    "Amount": clean_numeric_value(data["Amount"]),
                                    "Units": None,
                                    "NAV": None,
                                }
                            )
                            continue

    except Exception as e:
        print(f"CAUGHT GENERIC EXCEPTION: Type: {type(e)}, Message: {str(e)}")
        traceback.print_exc()
        return [], f"An unexpected error occurred during PDF processing: {str(e)}"

    # =====================================================================
    # POST-PROCESSING
    # =====================================================================
    # After extracting raw transaction rows, perform cleanup:
    #   1. Merge stamp duty amounts into the preceding transaction
    #   2. Remove STT (Securities Transaction Tax) rows (informational only)
    #   3. Classify each transaction as "Purchase" or "Redemption" based on sign

    processed_data_for_csv = []
    if not extracted_data_raw:
        return [], "No transaction data extracted from PDF."

    i = 0
    while i < len(extracted_data_raw):
        current_row = extracted_data_raw[i]

        # Stamp duty is a separate line but belongs to the previous transaction.
        # Merge its amount into the previous transaction's amount.
        if current_row["Transaction"] == "*** Stamp Duty ***":
            if current_row["Amount"] is not None and processed_data_for_csv:
                if processed_data_for_csv[-1]["Amount"] is None:
                    processed_data_for_csv[-1]["Amount"] = Decimal("0")
                processed_data_for_csv[-1]["Amount"] += current_row["Amount"]
            i += 1
            continue

        # STT (Securities Transaction Tax) is informational; skip it
        if current_row["Transaction"] == "*** STT Paid ***":
            i += 1
            continue

        # Classify transaction type based on the sign of the amount
        transaction_type = ""
        if current_row["Amount"] is not None:
            if current_row["Amount"] > 0:
                transaction_type = "Purchase"
            elif current_row["Amount"] < 0:
                transaction_type = "Redemption"
        current_row["Transaction Type"] = transaction_type

        processed_data_for_csv.append(current_row)
        i += 1

    if not processed_data_for_csv:
        return [], "No transaction data to write to CSV after processing."

    return processed_data_for_csv, "Success"


