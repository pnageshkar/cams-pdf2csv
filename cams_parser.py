def is_pdf_password_valid(
    file_path_or_bytes: Union[str, io.BytesIO], password: Optional[str]
) -> Optional[bool]:
    """
    Validate a PDF password without fully parsing the document.

    Uses pdfminer.six directly (rather than pdfplumber) for a lightweight check.
    This avoids the overhead of extracting text from all pages just to verify the password.

    Args:
        file_path_or_bytes: Absolute/relative path to the PDF file, or a file-like byte stream.
        password: Password to test. Use empty string or None for unprotected PDFs.

    Returns:
        True  - Password is correct (or PDF is unprotected and no password was needed).
        False - Password is incorrect.
        None  - File is not a valid PDF or another error occurred.
    """
    try:
        if isinstance(file_path_or_bytes, str):
            fs = open(file_path_or_bytes, "rb")
        else:
            fs = file_path_or_bytes
            fs.seek(0)

        parser = PDFParser(fs)
        doc = PDFDocument(parser, password=password or "")
        # Force password validation by attempting to access the first page.
        # PDFPasswordIncorrect is raised here if the password is wrong.
        next(PDFPage.create_pages(doc))

        if isinstance(file_path_or_bytes, str):
            fs.close()
        else:
            fs.seek(0)  # Reset stream position for the actual parsing later

        return True
    except PDFPasswordIncorrect:
        print("DEBUG: Incorrect password provided for PDF.")
        return False
    except Exception as e:
        if not isinstance(file_path_or_bytes, str):
            file_path_or_bytes.seek(0)
        print(f"DEBUG: Exception while checking PDF password: {type(e).__name__}: {e}")
        return None


def clean_numeric_value(value_str):
    """
    Parse a string representation of a number into a Decimal.

    Handles common formatting found in CAMS statements:
        - Comma-separated thousands: "1,234.56" → Decimal("1234.56")
        - Parenthesized negatives: "(500.00)" → Decimal("-500.00")
        - Placeholder dashes: "--" → None

    Args:
        value_str: The raw string from the PDF, or None.

    Returns:
        Decimal value, or None if the input is empty, a placeholder, or unparseable.
    """
    if value_str is None:
        return None
    text = str(value_str).strip()
    if text == "" or text == "--":
        return None
    # Remove thousands separators
    text = text.replace(",", "")
    # Convert parenthesized negatives: "(123.45)" → "-123.45"
    if text.startswith("(") and text.endswith(")"):
        text = "-" + text[1:-1]
    try:
        return Decimal(text)
    except InvalidOperation:
        return None


def remove_parenthetical_parts(text):
    """
    Clean up a fund name by removing parenthetical annotations.

    CAMS fund names often include annotations like "(Non-Demat)", "(formerly ...)",
    "(Advisor: DIRECT)", etc. This function strips all parenthesized content to
    produce a cleaner fund name for the CSV output.

    Example:
        "H02T-HDFC Flexi Cap Fund (Non-Demat)" → "H02T-HDFC Flexi Cap Fund"

    Args:
        text: Raw fund name string, or None.

    Returns:
        Cleaned fund name string, or None if input was None.
    """
    if text is None:
        return None
    text = re.sub(r"\s*\([^)]*\)", "", text)
    text = text.replace("(Non-Demat)", "").strip()
    return text.strip()


def extract_isin_from_text(text):
    """
    Extract an ISIN code from text containing an "ISIN:" label.

    Searches for the pattern "ISIN: <alphanumeric>" in the given text.
    If a complete 12-character ISIN is found, returns exactly 12 characters.
    If only a partial ISIN is found (due to PDF text truncation), returns
    the partial match so the caller can attempt to complete it.

    Args:
        text: A line of text that may contain "ISIN: INFxxxxxxxxx".

    Returns:
        - A 12-character ISIN string if a complete one is found.
        - A partial ISIN string if only a truncated one is found.
        - None if no "ISIN:" pattern is found at all.
    """
    if not text:
        return None
    # Find all "ISIN: <code>" patterns in the text (there may be multiple due to garbled lines)
    matches = re.findall(r"ISIN:\s*([A-Z0-9]+)", text)
    # Prefer the first complete (≥12 char) match
    for match in matches:
        if len(match) >= 12:
            return match[:12]
    # Fall back to the first partial match (caller will attempt completion)
    if matches:
        return matches[0]
    return None


def is_valid_isin(isin):
    """
    Validate that an ISIN is complete.

    Indian mutual fund ISINs are exactly 12 alphanumeric characters
    and always start with the prefix "INF".

    Args:
        isin: The ISIN string to validate, or None.

    Returns:
        True if the ISIN is exactly 12 characters and starts with "INF".
    """
    return isin is not None and len(isin) == 12 and isin.startswith("INF")


def prescan_isin_lookup(pdf_file_or_path, pdf_password=""):
    """
    Build a scheme-code → ISIN lookup table by pre-scanning all PDF pages.

    This is Layer 3 of the ISIN extraction strategy. It does a quick pass over all
    pages and collects ISINs from lines where they appear fully intact (12 characters).
    The resulting dictionary maps scheme codes (e.g., "H02T", "RMFGFAGG") to their ISINs.

    This lookup is used as a fallback when the ISIN on a specific fund's section
    is garbled due to PDF column merging. The same fund may appear on multiple pages
    (e.g., under different folios), and one of those occurrences may have an intact ISIN.

    Args:
        pdf_file_or_path: Path to the PDF file, or a file-like byte stream.
        pdf_password: Password for the PDF (empty string if unprotected).

    Returns:
        dict: Mapping of scheme code strings to 12-character ISIN strings.
              Empty dict if pre-scan fails or finds nothing.
    """
    # Match lines like: "H02T-HDFC Flexi Cap Fund ... ISIN: INF179K01UT0..."
    scheme_isin_regex = re.compile(
        r"^(?P<SchemeCode>[A-Z0-9]+)-.*?ISIN:\s*(?P<ISIN>[A-Z0-9]{12})"
    )
    lookup = {}
    try:
        with pdfplumber.open(pdf_file_or_path, password=pdf_password) as pdf:
            for page in pdf.pages:
                text_content = page.extract_text(
                    x_tolerance=3, y_tolerance=3, layout=False, keep_blank_chars=True
                )
                if not text_content:
                    continue
                for line in text_content.split("\n"):
                    line = line.strip()
                    m = scheme_isin_regex.match(line)
                    if m:
                        lookup[m.group("SchemeCode")] = m.group("ISIN")
    except Exception:
        pass  # If pre-scan fails, we still proceed with empty lookup

    if not isinstance(pdf_file_or_path, str):
        pdf_file_or_path.seek(0)

    return lookup


